package week1.day2;

public class PosorNeg {

	public static void main(String[] args) {
		
		int num = 42;
		
		if (num >0)
			
			System.out.println(num+ " is a Positive Number.");
		
		else if (num < 0)
			
			System.out.println(num+ " is a Negative Number.");
		
		else
			
			System.out.println(num+ " is neither Positive nor Negative.");
		
 }
	
	
 }	